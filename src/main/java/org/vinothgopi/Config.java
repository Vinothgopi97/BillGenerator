package org.vinothgopi;

public class Config {
    private String address;
    private String placeOfSupply;
    private String jioNumber;
    private String accountNo;
    private String statementNo;
    private String billingDate;
    private String dueDate;
    private String gstBillNo;
    private String registeredMobile;
    private String email;
    private String period;
    private String totalAmount;
    private double gstPercentage;
    private String fileName;
    private String aadhaar;

    // Getters and Setters

    public String getAadhaar() { return aadhaar; }
    public void setAadhaar(String aadhaar) { this.aadhaar = aadhaar; }

    public String getAddress() { return address; }
    public void setAddress(String address) { this.address = address; }

    public String getPlaceOfSupply() { return placeOfSupply; }
    public void setPlaceOfSupply(String placeOfSupply) { this.placeOfSupply = placeOfSupply; }

    public String getJioNumber() { return jioNumber; }
    public void setJioNumber(String jioNumber) { this.jioNumber = jioNumber; }

    public String getAccountNo() { return accountNo; }
    public void setAccountNo(String accountNo) { this.accountNo = accountNo; }

    public String getStatementNo() { return statementNo; }
    public void setStatementNo(String statementNo) { this.statementNo = statementNo; }

    public String getBillingDate() { return billingDate; }
    public void setBillingDate(String billingDate) { this.billingDate = billingDate; }

    public String getDueDate() { return dueDate; }
    public void setDueDate(String dueDate) { this.dueDate = dueDate; }

    public String getGstBillNo() { return gstBillNo; }
    public void setGstBillNo(String gstBillNo) { this.gstBillNo = gstBillNo; }

    public String getRegisteredMobile() { return registeredMobile; }
    public void setRegisteredMobile(String registeredMobile) { this.registeredMobile = registeredMobile; }

    public String getEmail() { return email; }
    public void setEmail(String email) { this.email = email; }

    public String getPeriod() { return period; }
    public void setPeriod(String period) { this.period = period; }

    public String getTotalAmount() { return totalAmount; }
    public void setTotalAmount(String totalAmount) { this.totalAmount = totalAmount; }

    public double getGstPercentage() { return gstPercentage; }
    public void setGstPercentage(double gstPercentage) { this.gstPercentage = gstPercentage; }

    public String getFileName() { return fileName; }
    public void setFileName(String fileName) { this.fileName = fileName; }
}

